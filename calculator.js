class Calculator {
  constructor() {}

  add(numberA, numberB) {
    return numberA + numberB;
  }

  substract(numberA, numberB) {
    return numberA - numberB;
  }

  divide(numberA, numberB) {
    return numberA / numberB;
  }

  multiply(numberA, numberB) {
    return numberA * numberB;
  }
}

module.exports = Calculator;
